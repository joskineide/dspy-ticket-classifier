"""
Milestone 17 — Datasets and Baseline Metrics

OBJECTIVE:
    Establish a mathematical baseline for how well the unoptimized module
    performs BEFORE any optimizer touches it. This number is your reference
    point — M18's value is only visible if you measured here first.

HOW TO RUN (from the project root, with venv active):
    python experiments/generate_dataset.py   # run once to generate the data
    python experiments/m17_baseline.py

CRITICAL — set lm_mode=direct in .env before running:
    The gateway's semantic cache will return the same cached answer for all
    similar tickets, collapsing the score to 1/N (random chance). Direct mode
    sends every evaluation call fresh to Ollama, bypassing cache entirely.
    This was discovered empirically: 33% on a 3-class problem = all responses
    identical, which is exactly what semantic cache does when all inputs are
    "customer support text".

DATASET CHOICE — why synthetic customer support data and not HuggingFace:
    We tried dair-ai/emotion (6 labels). Baseline was 62%, optimizers couldn't
    improve it. Root cause: semantic label overlap. joy/love and anger/sadness
    are inherently ambiguous in short text — even humans disagree on them.
    Collapsing to 3 sentiment labels (positive/negative/surprise) got to 83%
    but optimizer still made things worse — remaining errors were sarcasm/irony,
    which no prompt engineering can fix at this model size.

    Customer support text (bug/feature/feedback) works better because:
    1. The categories are structurally distinct, not emotionally adjacent
    2. qwen2.5:14b was likely trained on a lot of developer/support text
    3. "Something is broken" vs "I want something new" is almost never ambiguous

FINDINGS:
    Label design matters more than optimizer choice. Well-separated 3-class
    problem hit 91.67% baseline. The optimizer then had real signal to chase
    and pushed it to 100% with MIPROv2.

PRODUCTION NOTES (M17):
    Replace synthetic data with real labeled production traffic as early as
    possible. Even 50 human-labeled examples outperform 500 synthetic ones for
    the same model — the generator's biases become the training signal.

    Active learning: route low-confidence predictions (score near a label
    boundary, or a Refine retry was triggered) to a human review queue. Reviewed
    examples feed back into the dataset as high-signal training entries without
    annotation sprints.

    Every evaluation run should record dataset version, thresholds used, and
    scores — not just the final number. That audit trail makes regressions
    traceable to the specific data change that caused them.
"""

import sys
import os
import json
from typing import Any

sys.path.insert(0, os.path.dirname(os.path.dirname(__file__)))

import dspy

from app.services.classifier import configure_dspy, TicketClassifier


# ---------------------------------------------------------------------------
# 1. Dataset — loaded from synthetic_tickets.json
#
# Generated by experiments/generate_dataset.py using qwen2.5:14b through the
# gateway. 240 examples (80 per class), shuffled at generation time.
#
# Why synthetic rather than a real dataset?
#   - No public dataset has exactly the bug/feature/feedback taxonomy
#   - Real customer support datasets are domain-specific and often paywalled
#   - 240 synthetic examples with style variation gave the optimizer enough
#     signal to reach 100% — which is the goal of this learning exercise
#
# Caveat: synthetic data from the same model that classifies it creates a
# "closed loop". The generator's biases become the classifier's training signal.
# In production, even 50 human-labeled examples would outperform 500 synthetic
# ones for the same model. Use synthetic data to bootstrap, not as a substitute.
# ---------------------------------------------------------------------------

DATASET_PATH = os.path.join(os.path.dirname(__file__), "synthetic_tickets.json")


def load_examples() -> list[dspy.Example]:
    if not os.path.exists(DATASET_PATH):
        raise FileNotFoundError(
            f"Dataset not found at {DATASET_PATH}. "
            "Run `python experiments/generate_dataset.py` first."
        )
    with open(DATASET_PATH, encoding="utf-8") as f:
        rows = json.load(f)
    return [
        dspy.Example(ticket=row["ticket"], categories=row["categories"]).with_inputs("ticket")
        for row in rows
    ]


EXAMPLES = load_examples()

split = int(len(EXAMPLES) * 0.7)
TRAINSET = EXAMPLES[:split]   # optimizer sees these and bootstraps from them
DEVSET   = EXAMPLES[split:]   # held out — honest measure of generalisation

print(f"Dataset loaded: {len(TRAINSET)} train / {len(DEVSET)} dev examples")


# ---------------------------------------------------------------------------
# 2. Metric — Jaccard similarity
#
# For single-label, this degenerates to exact match (1.0 or 0.0).
# We keep Jaccard rather than a simpler exact-match function so the metric
# works unchanged if the dataset is ever expanded to multi-label.
#
# The metric is the single most important design decision in a DSPy pipeline.
# Whatever it measures is what the optimizer maximises. Wrong metric = the
# optimizer confidently improves the wrong thing.
#
# Output type         Metric
# Single label        Jaccard (= exact match when single)
# Multi-label         Jaccard (partial credit for partial overlap)
# Ranked list         NDCG
# Free text           ROUGE, BERTScore
# Code                Execute + assert
# Subjective output   LLM-as-judge
# ---------------------------------------------------------------------------

def jaccard_metric(example: dspy.Example, pred: dspy.Prediction, trace=None) -> float:
    example_set: set[Any] = set(example.categories)
    pred_set: set[Any] = set(pred.categories)
    intersection = example_set & pred_set
    union = example_set | pred_set
    if not union:
        return 0.0
    return len(intersection) / len(union)


# ---------------------------------------------------------------------------
# 3. Baseline evaluation
# ---------------------------------------------------------------------------

if __name__ == "__main__":
    configure_dspy()

    module = TicketClassifier()

    evaluate = dspy.Evaluate(
        devset=DEVSET,
        metric=jaccard_metric,
        num_threads=1,
        display_progress=True,
        display_table=5,
    )

    baseline_score = evaluate(module)
    print(f"\nBaseline Jaccard score: {float(baseline_score):.3f}")
    print("Write this number down — M18's optimizer will try to beat it.")
